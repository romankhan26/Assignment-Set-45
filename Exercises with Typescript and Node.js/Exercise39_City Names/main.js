"use strict";
function city_country(city, country) {
    return `${city},${country}`;
}
const city1 = city_country("Karachi", "Pakistan");
const city2 = city_country("Doha", "Qatar");
const city3 = city_country("New York", "USA");
const city4 = city_country("Tokyo", "Japan");
console.log(city1);
console.log(city2);
console.log(city3);
console.log(city4);
